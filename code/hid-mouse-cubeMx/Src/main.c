  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	if (LIS3DSH_PollDRDY(100)) {
		myRawData = LIS3DSH_GetDataRaw();
		
		mouseBuffer[0] = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0);
		mouseBuffer[1] = (myRawData.x >> 8);
		mouseBuffer[2] = -(myRawData.y >> 8);

		USBD_CUSTOM_HID_SendReport(&hUsbDeviceFS, mouseBuffer, 4);
		HAL_Delay(100);
	}
  }
